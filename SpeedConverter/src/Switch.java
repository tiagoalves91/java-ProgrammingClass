public class Switch {

    public static void switch123() {

        char letter = 'X';

        switch (letter) {
            case 'A':
                System.out.println("is one");
                break;
            case 'B':
                System.out.println("is 2");
                break;
            case 'D':
                System.out.println("is 3");
                break;
            case 'X':
                System.out.println("is 4");
                break;
            default:
            System.out.println("is 5");
                break;
        }

    }
}
